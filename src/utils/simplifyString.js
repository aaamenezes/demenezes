export function simplifyString(string) {
  return string.toLowerCase()
    .split('á')
    .join('a')
    .split('à')
    .join('a')
    .split('é')
    .join('e')
    .split('ê')
    .join('e')
    .split('í')
    .join('i')
    .split('ó')
    .join('o')
    .split('ô')
    .join('o')
    .split('õ')
    .join('o')
    .split('ú')
    .join('u')
    .split('ç')
    .join('c')
    .split('0')
    .join('')
    .split('1')
    .join('')
    .split('2')
    .join('')
    .split('3')
    .join('')
    .split('4')
    .join('')
    .split('5')
    .join('')
    .split('6')
    .join('')
    .split('7')
    .join('')
    .split('8')
    .join('')
    .split('9')
    .join('-')
    .split('_')
    .join('-')
    .split('.')
    .join('-')
    .split(',')
    .join('-')
    .split('/')
    .join('-')
    .split('(')
    .join('-')
    .split(')')
    .join('-')
    .trim()
}
